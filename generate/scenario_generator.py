    DEFAULT_SEVERITY: dict[str, ScenarioSeverity] = {
        "functional": ScenarioSeverity.MEDIUM,
        "security": ScenarioSeverity.HIGH,
        "performance": ScenarioSeverity.LOW,
        "compliance": ScenarioSeverity.MEDIUM,
        "integration": ScenarioSeverity.MEDIUM,
    }

    def __init__(
        self,
        *,
        seed: Optional[int] = None,
        templates: Optional[dict[str, list[dict[str, Any]]]] = None,
    ) -> None:
        self.seed = seed
        self.rng = random.Random(seed)
        self.templates = templates or self.TEMPLATES

        logger.debug(
            "ScenarioGenerator initialised (seed=%s, templates=%d categories)",
            seed,
            len(self.templates),
        )

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def generate(
        self,
        count: int = 5,
        *,
        target: str = "/api/endpoint",
        categories: Optional[list[ScenarioCategory]] = None,
        severity: Optional[ScenarioSeverity] = None,
        tags: Optional[list[str]] = None,
    ) -> list[Scenario]:
        """Generate a list of test scenarios.

        Args:
            count:      Number of scenarios to produce (default 5).
            target:     Target URL or component name to bake into scenarios.
            categories: Filter by one or more ``ScenarioCategory`` values.
                        If ``None``, all categories are eligible.
            severity:   If set, only templates whose default severity matches
                        are used.
            tags:       If set, only templates containing **any** of the
                        given tags are used.

        Returns:
            A list of ``Scenario`` instances.

        Raises:
            ValueError: If ``count`` is less than 1.
        """
        if count < 1:
            raise ValueError("count must be >= 1")

        logger.info(
            "Generating %d scenarios (target=%s, categories=%s, severity=%s, tags=%s)",
            count,
            target,
            categories,
            severity,
            tags,
        )

        # Flatten eligible templates -------------------------------------------------
        candidates: list[tuple[ScenarioCategory, dict[str, Any]]] = []

        for cat_name, templates in self.templates.items():
            cat_enum = ScenarioCategory(cat_name)

            if categories and cat_enum not in categories:
                continue

            for template in templates:
                t_sev = self.DEFAULT_SEVERITY.get(cat_name, ScenarioSeverity.MEDIUM)

                if severity and t_sev != severity:
                    continue

                if tags:
                    ttags = [t.lower() for t in template.get("tags", [])]
                    if not any(t.lower() in ttags for t in tags):
                        continue

                candidates.append((cat_enum, template))

        if not candidates:
            logger.warning("No templates matched the given filters — returning empty list")
            return []

        # Sample and build scenarios -------------------------------------------------
        chosen: list[tuple[ScenarioCategory, dict[str, Any]]]
        if len(candidates) <= count:
            chosen = candidates[:]
            self.rng.shuffle(chosen)
        else:
            chosen = self.rng.sample(candidates, k=count)

        scenarios: list[Scenario] = []
        for cat, template in chosen:
            scenario = self._build_scenario(cat, template, target)
            scenarios.append(scenario)

        logger.info("Generated %d scenarios", len(scenarios))
        return scenarios

    def generate_from_file(
        self,
        path: str | Path,
        *,
        target: str = "/api/endpoint",
        **kwargs: Any,
    ) -> list[Scenario]:
        """Load templates from a JSON file and generate scenarios.

        The JSON file must contain a dict with category names as keys,
        each value being a list of template dicts (same structure as
        ``TEMPLATES``).

        Args:
            path:   Path to a JSON template file.
            target: Target to inject into templates.
            **kwargs: Forwarded to ``generate()``.

        Returns:
            List of generated ``Scenario`` instances.

        Raises:
            FileNotFoundError: If the path does not exist.
            ValueError:       If the JSON is malformed.
        """
        p = Path(path)
        if not p.exists():
            raise FileNotFoundError(f"Template file not found: {p.resolve()}")

        try:
            raw = p.read_text(encoding="utf-8")
            data: dict[str, list[dict[str, Any]]] = json.loads(raw)
        except (json.JSONDecodeError, OSError) as exc:
            raise ValueError(f"Failed to parse template file: {exc}") from exc

        logger.info("Loaded %d template categories from %s", len(data), p.resolve())
        return self.generate(count=len(data) * 3, target=target, **kwargs)

    def export_to_json(
        self,
        scenarios: list[Scenario],
        path: str | Path,
        *,
        indent: int = 2,
    ) -> Path:
        """Export a list of scenarios to a JSON file.

        Args:
            scenarios: Scenarios to export.
            path:      Destination file path.
            indent:    JSON indentation level (default 2).

        Returns:
            The resolved ``Path`` to the written file.
        """
        p = Path(path)
        data = [s.to_dict() for s in scenarios]
        p.write_text(json.dumps(data, indent=indent, default=str), encoding="utf-8")
        logger.info("Exported %d scenarios to %s", len(scenarios), p.resolve())
        return p.resolve()

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _build_scenario(
        self,
        category: ScenarioCategory,
        template: dict[str, Any],
        target: str,
    ) -> Scenario:
        """Instantiate a ``Scenario`` from a template dict."""
        name = template["name"].format(target=target)
        description = template["description"].format(target=target)

        steps: list[ScenarioStep] = []
        for step_def in template.get("steps", []):
            action = step_def["action"].format(target=target, payload=self._random_payload())
            steps.append(
                ScenarioStep(
                    action=action,
                    expected=step_def["expected"],
                    params=dict(step_def.get("params", {})),
                )
            )

        ttags = list(template.get("tags", []))
        severity = self.DEFAULT_SEVERITY.get(category.value, ScenarioSeverity.MEDIUM)

        return Scenario(
            name=name,
            category=category,
            severity=severity,
            target=target,
            description=description,
            steps=steps,
            tags=ttags,
            metadata={
                "generator": "ScenarioGenerator",
                "seed": self.seed,
                "template_name": template.get("name", ""),
            },
        )

    def _random_payload(self) -> str:
        """Generate a random payload string for injection templates."""
        payloads = [
            "' OR '1'='1",
            "<script>alert(1)</script>",
            "../../etc/passwd",
            "'; DROP TABLE users; --",
            "\" ONMOUSEOVER=\"alert(1)\"",
        ]
        return self.rng.choice(payloads)
