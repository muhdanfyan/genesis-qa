"""
Genesis QA - Scenario Generator Module
========================================
Generates structured, configurable test scenarios suitable for running
against the scan pipeline.

Typical usage:
    generator = ScenarioGenerator()
    scenarios = generator.generate(count=10)
"""

from __future__ import annotations

import json
import logging
import random
import uuid
from dataclasses import dataclass, field, asdict
from datetime import datetime, timezone
from enum import Enum
from pathlib import Path
from typing import Any, Optional

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Types
# ---------------------------------------------------------------------------


class ScenarioCategory(Enum):
    """High-level category a scenario belongs to."""

    FUNCTIONAL = "functional"
    SECURITY = "security"
    PERFORMANCE = "performance"
    COMPLIANCE = "compliance"
    INTEGRATION = "integration"


class ScenarioSeverity(Enum):
    """Severity / priority level of a test scenario."""

    CRITICAL = "critical"
    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"


@dataclass
class ScenarioStep:
    """A single step within a test scenario."""

    action: str
    expected: str
    params: dict[str, Any] = field(default_factory=dict)


@dataclass
class Scenario:
    """A complete test scenario generated by the ScenarioGenerator.

    Attributes:
        uid:         Unique scenario identifier.
        name:        Human-readable scenario name.
        category:    Functional / security / performance / etc.
        severity:    Priority / severity rating.
        target:      The target endpoint, URL, or component under test.
        description: Short description of what the scenario validates.
        steps:       Ordered list of actions and expected outcomes.
        tags:        Arbitrary labels for filtering / grouping.
        metadata:    Generation-time diagnostics (template used, seed, etc.).
        created_at:  ISO-formatted UTC timestamp.
    """

    uid: str = ""
    name: str = ""
    category: ScenarioCategory = ScenarioCategory.FUNCTIONAL
    severity: ScenarioSeverity = ScenarioSeverity.MEDIUM
    target: str = ""
    description: str = ""
    steps: list[ScenarioStep] = field(default_factory=list)
    tags: list[str] = field(default_factory=list)
    metadata: dict[str, Any] = field(default_factory=dict)
    created_at: str = ""

    def __post_init__(self) -> None:
        if not self.uid:
            self.uid = uuid.uuid4().hex[:12]
        if not self.created_at:
            self.created_at = datetime.now(timezone.utc).isoformat()

    def to_dict(self) -> dict[str, Any]:
        """Serialize the scenario to a JSON-safe dictionary."""
        d = asdict(self)
        d["category"] = self.category.value
        d["severity"] = self.severity.value
        return d

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> Scenario:
        """Deserialize a dictionary back into a Scenario."""
        data = dict(data)
        data["category"] = ScenarioCategory(data.get("category", "functional"))
        data["severity"] = ScenarioSeverity(data.get("severity", "medium"))
        data["steps"] = [ScenarioStep(**s) for s in data.get("steps", [])]
        return cls(**data)


# ---------------------------------------------------------------------------
# Generator
# ---------------------------------------------------------------------------


